export function getCountriesWithPriority(locale: 'fr' | 'en' = 'fr') {
  const otherCountries = ALL_COUNTRIES.filter(
    country => !PRIORITY_COUNTRIES.includes(country)
  ).sort();

  const pick = (country: string) => ({
    value: country, // Store the untranslated country name
    label: countryTranslations[country]?.[locale] ?? country, // Display the translated name
  });

  return {
    priority: PRIORITY_COUNTRIES.map(pick),
    others: otherCountries.map(pick),
  };
}

export function getNationalitiesWithPriority(locale: 'fr' | 'en') {
  const { priority, others } = getCountriesWithPriority(locale);
  const pick = (country: { value: string; label: string }) => ({
    value: country.value, // Store the untranslated country name
    label: nationalities[country.value]?.[locale] ?? country.label, // Display the adjective
  });

  return {
    priority: priority.map(pick),
    others: others.map(pick),
  };
}